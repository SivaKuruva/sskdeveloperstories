package main.com.ssk.javarefresher.basics;

public class AssignmentOperator {
    public static void main(String[] args) {
        // = : assignment
        int a=10,b=20;
        System.out.println("a="+a+" b="+b);

        // += : right addition
        a+=b;
        System.out.println("a="+a+" b="+b);

        // -= : right subtraction
        a-=b;
        System.out.println("a="+a+" b="+b);

        // *= : right multiplication
        a*=b;
        System.out.println("a="+a+" b="+b);

        // /= : right division
        a/=b;
        System.out.println("a="+a+" b="+b);

        // %= : right modulos
        a%=b;
        System.out.println("a="+a+" b="+b);
    }
}
