package main.com.ssk.java.basics;

public class PrimitiveDataTypes {
    public static void main(String[] args) {
        //boolean - 1 bit
        boolean flag=true;

        //byte - 8 bits
        byte b = 127;

        // short - 16 bit
        short s= 32767;

        // integer - 32 bit
        int i = 2147483647;

        // long - 64 bit
        long l=-9848020304L;

        // float - 32 bit
        float f=-12.2f;

        // double - 64 bit
        double d = 160000.00d;

        System.out.println(flag +" - Boolean");
        System.out.println(b +" - Byte");
        System.out.println(s +" - Short");
        System.out.println(i +" - Int");
        System.out.println(l +" - Long");
        System.out.println(f +" - Float");
        System.out.println(d +" - Double");
    }
}
